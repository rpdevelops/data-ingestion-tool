-- =========================
-- ENUM TYPES
-- =========================

CREATE TYPE job_status_enum AS ENUM (
  'PENDING',
  'PROCESSING',
  'NEEDS_REVIEW',
  'COMPLETED',
  'FAILED'
);

CREATE TYPE issue_type_enum AS ENUM (
  'DUPLICATE_EMAIL',
  'INVALID_EMAIL',
  'EXISTING_EMAIL',
  'MISSING_REQUIRED_FIELD'
);

CREATE TYPE staging_status_enum AS ENUM (
  'READY',
  'SUCCESS',
  'DISCARD',
  'ISSUE'
);

-- =========================
-- JOBS
-- =========================

CREATE TABLE public.jobs (
  job_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  job_created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  job_user_id VARCHAR NOT NULL,
  job_original_filename VARCHAR NOT NULL,
  job_s3_object_key VARCHAR NOT NULL,
  job_status job_status_enum NOT NULL,
  job_total_rows INTEGER NOT NULL,
  job_processed_rows INTEGER NOT NULL,
  job_issue_count INTEGER NOT NULL,
  job_process_start TIMESTAMPTZ,
  job_process_end TIMESTAMPTZ
);

-- =========================
-- ISSUES
-- =========================

CREATE TABLE public.issues (
  issue_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  issues_job_id INTEGER NOT NULL,
  issue_type issue_type_enum NOT NULL,
  issue_key VARCHAR NOT NULL,
  issue_resolved BOOLEAN NOT NULL DEFAULT false,
  issue_description VARCHAR,
  issue_resolved_at TIMESTAMPTZ,
  issue_resolved_by VARCHAR,
  issue_resolution_comment VARCHAR,
  issue_created_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT jobs_issues_fk
    FOREIGN KEY (issues_job_id)
    REFERENCES public.jobs (job_id)
    ON DELETE CASCADE
);

CREATE UNIQUE INDEX issues_job_type_key_uq
  ON public.issues (issues_job_id, issue_type, issue_key);

-- =========================
-- STAGING
-- =========================

CREATE TABLE public.staging (
  staging_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  staging_job_id INTEGER NOT NULL,
  staging_email VARCHAR,
  staging_first_name VARCHAR,
  staging_last_name VARCHAR,
  staging_company VARCHAR,
  staging_created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  staging_status staging_status_enum,
  staging_row_hash VARCHAR NOT NULL,

  CONSTRAINT jobs_staging_fk
    FOREIGN KEY (staging_job_id)
    REFERENCES public.jobs (job_id)
    ON DELETE CASCADE
);

CREATE UNIQUE INDEX staging_job_hash_uq
  ON public.staging (staging_job_id, staging_row_hash);

-- =========================
-- ISSUE ITEMS
-- =========================

CREATE TABLE public.issue_items (
  issue_item_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  item_issue_id INTEGER NOT NULL,
  item_staging_id BIGINT NOT NULL,

  CONSTRAINT issues_issue_items_fk
    FOREIGN KEY (item_issue_id)
    REFERENCES public.issues (issue_id)
    ON DELETE CASCADE,

  CONSTRAINT staging_issue_items_fk
    FOREIGN KEY (item_staging_id)
    REFERENCES public.staging (staging_id)
    ON DELETE CASCADE
);

-- =========================
-- CONTACTS
-- =========================

CREATE TABLE public.contacts (
  contact_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  staging_id BIGINT NOT NULL,
  contacts_user_id VARCHAR NOT NULL,
  contact_email VARCHAR NOT NULL,
  contact_first_name VARCHAR NOT NULL,
  contact_last_name VARCHAR NOT NULL,
  contact_company VARCHAR NOT NULL,
  contact_created_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT staging_contacts_fk
    FOREIGN KEY (staging_id)
    REFERENCES public.staging (staging_id)
    ON DELETE CASCADE
);
